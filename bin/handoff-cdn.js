  for (const c of checks) stdout.write(`  ${c.pass ? '✓' : '✗'} ${c.name.padEnd(26)} ${c.detail}\n`);

  const failures = checks.filter(c => !c.pass);
  if (failures.length) {
    stdout.write(`\n  ${failures.length} check${failures.length > 1 ? 's' : ''} failed — cannot push.\n\n`);
    exit(1);
  }

